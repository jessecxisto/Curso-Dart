main() {
  // Operadores Aritméticos
  int a = 7;
  int b = 3;
  int resultado = a + b;

  print(resultado);

  print(a + b); // Adição
  print(a - b); // Subtração
  print(a * b); // Multiplicação
  print(a / b); // Divisão
  print(a % b); // Módulo (Resto da divisão)

  // Operadores Lógicos
  bool ehFragil = true;
  bool ehCaro = false;

  print(ehFragil && ehCaro); // E
  print(ehFragil || ehCaro); // OU
}
