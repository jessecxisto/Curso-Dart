main() {
  // Operadores de Atribuição
  double a = 1;
  print(a);
  a = a + 2;
  print(a);
  a += 3;
  print(a);
  a -= 3;
  print(a);
  a *= 3;
  print(a);
  a /= 3;
  print(a);
  a %= 3;
  print(a);

  // Operadores Relacionais -> O resultado sempre é BOOL
  print(3 > 2); // Maior
  print(3 >= 3); // Maior ou igual
  print(3 < 4); // Menor
  print(3 <= 3); // Menor ou igual
  print(3 != 3); // Diferente
  print(3 == 3); // Igual
}
